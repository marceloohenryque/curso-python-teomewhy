# Tipos de Dados
#%%
# Tipo de dados - Numérico: Inteiro
print("Inteiros: ")
print(1)
print(2 + 2)

#%%
# Tipo de dados - Numérico: Float (Tipo Real)
print("Float: ")
print(1.)
print(4.9 + 1)
print(4 / 2)

#%%
# Tipo de dados: Booleano
# O Booleano pode ser o resultado de uma comparação ou operação lógica de variáveis.
print("Booleano: ")
print(True)
print(False)
print(bool(1))
print(bool(0))