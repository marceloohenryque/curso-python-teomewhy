# Equivalente ao arquivo calculadora da aula
# Aprendendo sobre operações aritméticas

#%%
# SOMA
print("1 + 1 = ", 1 + 1)

#%%
# SUBTRAÇÃO
print("10 - 5 = ", 10 - 5)

#%%
# MULTIPLICAÇÃO
print("5 * 2 = ", 5 * 2)


#%%
# DIVISÃO
# 1. DIVISÃO NORMAL
print("10 / 5 = ", 10 / 5)

# 2. DIVISÃO DE PARTE INTEIRA
print("10 / 3 = ", 10 // 3)

# 3. RESTO DA DIVISÃO
print("10 / 4 = ", 10 % 4)

#%%
# POTÊNCIA
print("3² = ", 3 ** 2)