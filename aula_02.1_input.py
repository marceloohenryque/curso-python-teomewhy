# Aprendendo sobre recebimento de dados do usuário
# Saída do usuário
print("Hello World!")

# A função input pede para que o usuário digite alguma coisa no teclado
nome = input("Qual o seu nome? ") # Esse valor digitado será atribuído na variável nome
print(f"Olá {nome}, prazer em conhecer-te!")    # A letra f dentro do () indica que está usando uma f-string (formatted string literals)
                                                # Uma f-string incorpora expressões e variáveis de maneira mais fácil usando chaves {}
